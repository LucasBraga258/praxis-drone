CREATE POLICY "Deus_da_Plataforma_Full_Access_Notificacoes" ON public.notificacoes FOR ALL USING (EXISTS (SELECT 1 FROM perfis_usuarios WHERE id = auth.uid() AND perfil = 'admin'));

