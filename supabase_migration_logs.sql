CREATE POLICY "Deus_da_Plataforma_Full_Access_ProcessLogs" ON public.process_logs FOR ALL USING (EXISTS (SELECT 1 FROM perfis_usuarios WHERE id = auth.uid() AND perfil = 'admin'));

ALTER TABLE public.notificacoes ENABLE ROW LEVEL SECURITY;
